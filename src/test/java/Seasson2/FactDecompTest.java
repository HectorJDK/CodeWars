package Seasson2;

import static org.junit.Assert.assertEquals;

import org.junit.Test;

public class FactDecompTest {

    private static void testing(int n, String expected) {
	System.out.println("N: " + n);
	String actual = FactDecomp.decomp(n);
	assertEquals(expected, actual);
    }

    @Test
    public void test1() {
	testing(17, "2^15 * 3^6 * 5^3 * 7^2 * 11 * 13 * 17");
	testing(5, "2^3 * 3 * 5");
	testing(22, "2^19 * 3^9 * 5^4 * 7^3 * 11^2 * 13 * 17 * 19");
	testing(14, "2^11 * 3^5 * 5^2 * 7^2 * 11 * 13");
	testing(25, "2^22 * 3^10 * 5^6 * 7^3 * 11^2 * 13 * 17 * 19 * 23");

	testing(45, "2^41 * 3^21 * 5^10 * 7^6 * 11^4 * 13^3 * 17^2 * 19^2 * 23 * 29 * 31 * 37 * 41 * 43");
	testing(48, "2^46 * 3^22 * 5^10 * 7^6 * 11^4 * 13^3 * 17^2 * 19^2 * 23^2 * 29 * 31 * 37 * 41 * 43 * 47");
	testing(35, "2^32 * 3^15 * 5^8 * 7^5 * 11^3 * 13^2 * 17^2 * 19 * 23 * 29 * 31");
	testing(23, "2^19 * 3^9 * 5^4 * 7^3 * 11^2 * 13 * 17 * 19 * 23");
	testing(35, "2^32 * 3^15 * 5^8 * 7^5 * 11^3 * 13^2 * 17^2 * 19 * 23 * 29 * 31");

	testing(79,
		"2^74 * 3^36 * 5^18 * 7^12 * 11^7 * 13^6 * 17^4 * 19^4 * 23^3 * 29^2 * 31^2 * 37^2 * 41 * 43 * 47 * 53 * 59 * 61 * 67 * 71 * 73 * 79");
	testing(66,
		"2^64 * 3^31 * 5^15 * 7^10 * 11^6 * 13^5 * 17^3 * 19^3 * 23^2 * 29^2 * 31^2 * 37 * 41 * 43 * 47 * 53 * 59 * 61");
	testing(98,
		"2^95 * 3^46 * 5^22 * 7^16 * 11^8 * 13^7 * 17^5 * 19^5 * 23^4 * 29^3 * 31^3 * 37^2 * 41^2 * 43^2 * 47^2 * 53 * 59 * 61 * 67 * 71 * 73 * 79 * 83 * 89 * 97");
	testing(95,
		"2^89 * 3^45 * 5^22 * 7^14 * 11^8 * 13^7 * 17^5 * 19^5 * 23^4 * 29^3 * 31^3 * 37^2 * 41^2 * 43^2 * 47^2 * 53 * 59 * 61 * 67 * 71 * 73 * 79 * 83 * 89");
	testing(70,
		"2^67 * 3^32 * 5^16 * 7^11 * 11^6 * 13^5 * 17^4 * 19^3 * 23^3 * 29^2 * 31^2 * 37 * 41 * 43 * 47 * 53 * 59 * 61 * 67");
    }
}
